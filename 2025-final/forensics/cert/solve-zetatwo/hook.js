let randoffset = 0;

const syscall_hook = Interceptor.attach(new NativePointer(addr_syscall), {
    onEnter(args) {
        if(this.context.rax == 0x13e) {
            const buflen = this.context.rcx
            const bufaddr = this.context.rbx
            this.getrandom = {
                len: buflen,
                buf: bufaddr
            }
            //console.log('sys_getrandom', bufaddr, buflen)
        }
    },
    onLeave() {
        if(this.getrandom) {
            const len = this.getrandom.len.toInt32()
            console.log('sys_getrandom leave', len, this.getrandom.buf, randoffset)
            
            if(randoffset+1 < randdata.length) {
                this.getrandom.buf.writeByteArray(randdata.slice(randoffset, randoffset+len))
                randoffset += len;
            }
        }
    }
});
